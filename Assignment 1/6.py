# WAP to Find data type for Particular Variable[integer,Boolean,float,string, complex]

integer = 20
float = 5.11
boolean = True
string = "Hello World"
complex = 20 + 20j

print(f"The data type of integer is : {type(integer)}")
print(f"The data type of float is : {type(float)}")
print(f"The data type of boolean is : {type(boolean)}")
print(f"The data type of string is : {type(string)}")
print(f"The data type of complex is : {type(complex)}")